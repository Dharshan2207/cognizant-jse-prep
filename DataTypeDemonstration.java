public class DataTypeDemonstration {
    public static void main(String[] args) {
        int integer;
        char character;
        float floatExample;
        double doubleExample;
        boolean bool;

        integer = 10;
        character = 'A';
        floatExample = 12.42f;
        doubleExample = 4.5;
        bool = false;

        System.out.println("Integer example : " + integer);
        System.out.println("Character example : " + character);
        System.out.println("Float example : " + floatExample);
        System.out.println("Double example : " + doubleExample);
        System.out.println("Boolean example : " + bool);
    }
}
